"""
  Classification of a backend variable.

  Variants actually constructed by the lowering / causalization pipeline:

  - `VARIABLE` — default algebraic unknown.
  - `STATE(index, derName, natural)`:
    - `index`: how often this state was differentiated.
    - `derName`: name of the derivative component reference, if known.
    - `natural`: `false` if the state was forced by `StateSelect.always` /
      `StateSelect.prefer` or generated by index reduction.
  - `STATE_DER` — state derivative (`der(x)`).
  - `DUMMY_DER` — dummy derivative introduced by Pantelides index reduction.
  - `DUMMY_STATE` — dummy state paired with a `DUMMY_DER`.
  - `DISCRETE` — discrete-time variable (changes only on events).
  - `PARAM` — parameter (constant for a single simulation run).
  - `CONST` — compile-time constant.

  Additional variants present for OpenModelica-compat but not produced by
  this backend (kept so `backendDump.jl` can pretty-print externally
  supplied BDAE fragments): `CLOCKED_STATE(previousName, isStartFixed)`,
  `EXTOBJ(fullClassName)`, `JAC_VAR`, `JAC_DIFF_VAR`, `SEED_VAR`,
  `OPT_CONSTR`, `OPT_FCONSTR`, `OPT_INPUT_WITH_DER`, `OPT_INPUT_DER`,
  `OPT_TGRID`, `OPT_LOOP_INPUT(replaceExp)`, `ALG_STATE`, `ALG_STATE_OLD`,
  `DAE_RESIDUAL_VAR`, `DAE_AUX_VAR`, `LOOP_ITERATION`, `LOOP_SOLVED`.
"""
@UniontypeDecl VarKind

"""
  Per-equation kind tag used inside [`EquationAttributes`](@ref).

  Variants: `DYNAMIC_EQUATION` (dynamic / simulation-time) and
