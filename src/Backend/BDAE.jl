"""
  The concrete backend variable record.

  Fields:
  - `varName`: the component reference this variable stands for.
  - `varKind`: state / algebraic / parameter / constant / discrete / ...
  - `varDirection`: input, output, or bidirectional.
  - `varType`: scalar built-in type or enumeration type.
  - `bindExp`: optional binding expression (e.g. parameter default).
  - `arryDim`: array dimensions when the variable is not yet scalarized.
  - `source`: origin metadata (file, line, comment) from the front end.
  - `values`: optional built-in attribute bundle (start, min, max, nominal, ...).
  - `tearingSelectOption`: legacy slot retained for binary compatibility;
    tearing is not currently performed inside OMBackend.
  - `connectorType`: flow / stream / unspecified / not-a-connector.
  - `unreplaceable`: guard against being substituted out by alias elimination.
"""
mutable struct VAR <: Var
  varName::DAE.ComponentRef
  varKind::VarKind
  varDirection::DAE.VarDirection
  varType::DAE.Type
  bindExp::Option
  arryDim::List
  source::DAE.ElementSource
  values::Option
  tearingSelectOption::Option
  connectorType::DAE.ConnectorType
  unreplaceable::Bool
end

"""
  Convenience constructor: a bidirectional, non-connector, replaceable VAR
  with no binding, no dimensions, and no attribute bundle.
"""
VAR(varName,varKind,varType) =
  let
    VAR(varName, varKind, DAE.BIDIR(), varType, NONE(), nil, DAE.emptyElementSource,
        NONE(), NONE(), DAE.NON_CONNECTOR(), true #= Unreplaceable =#)
  end

"""
  An independent system of equations together with its variables.

  Each structural submodel (VSS branch, DOCC subsystem) becomes one
  `EQSYSTEM`. The `simpleEquations` field holds alias equations that have
  already been removed from the main equation list via alias elimination;
  they are kept so downstream stages can recover the original mapping.

  Fields:
  - `name`: identifier for the subsystem (typically the model or
    submodel class name).
  - `orderedVars`: states and algebraic unknowns in declaration order.
    Parameters and constants live on [`SHARED`](@ref) instead.
  - `orderedEqs`: the main equation list, aligned 1:1 with `orderedVars`
    after matching.
  - `simpleEquations`: alias equations already eliminated from the main
    list (kept for round-trip / reporting).
  - `initialEqs`: equations that only apply at initialization.
"""
mutable struct EQSYSTEM
  name::String
  orderedVars::Vector{VAR}
  orderedEqs::Vector{Equation}
  simpleEquations::Vector{Equation}
  initialEqs::Vector{Equation}
end

"""
  State shared between all equation systems of a single model.

  Fields:
  - `globalKnownVars`: parameters / constants visible in every subsystem.
  - `localKnownVars`: parameters / constants scoped to a specific submodel.
  - `metaModel`: optional reference to the surrounding meta-model; `NONE()`
    for non-VSS models.
  - `flatModel`: optional reference to the flat model being translated.
    Used by VSS recompilation paths.
  - `DOCC_equations`: dynamic-if equations from the Dynamic Overconstrained
    Connector option.
"""
struct SHARED
  globalKnownVars::Vector{VAR}
  localKnownVars::Vector{VAR}
  metaModel::Option
  flatModel::Option
  DOCC_equations::Vector{Equation}
end

"""
  Top-level lowered DAE container: one or more `EQSYSTEM`s plus `SHARED`
  knowledge. This is what the causalization pipeline consumes and what the
  code generator translates into an MTK `ODESystem`.
"""
mutable struct BACKEND_DAE
  name::String
  eqs::Vector{EQSYSTEM}
  shared::SHARED
end

