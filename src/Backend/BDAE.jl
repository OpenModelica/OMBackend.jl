@UniontypeDecl EquationKind

"""
  Flags controlling which simulation stages an equation contributes to.

  Single variant:

  - `EVALUATION_STAGES(dynamicEval, algebraicEval, zerocrossEval, discreteEval)`
    — four `Bool` flags, one per stage.
"""
@UniontypeDecl EvaluationStages

"""
  Equation metadata wrapper.

  Variants:

  - `EQUATION_ATTRIBUTES(differentiated, kind, evalStages)`:
    - `differentiated`: `true` if the equation was produced by symbolic
      differentiation. Guards against being differentiated twice.
    - `kind`: an [`EquationKind`](@ref) tag.
    - `evalStages`: an [`EvaluationStages`](@ref) flag set.
  - `NO_ATTRIBUTES` — sentinel used when attributes are unknown or not
    yet assigned.
"""
@UniontypeDecl EquationAttributes

"""
  Sum type for backend equations.

  Variants and their fields:

  - `EQUATION(lhs, rhs, source, attributes)` — plain equality `lhs = rhs`.
  - `ARRAY_EQUATION(dimSize, left, right, source, attr, recordSize)` —
    array-shaped equation. `dimSize` holds the array dimension sizes;
    `recordSize` is `NONE()` for non-record arrays.
  - `SOLVED_EQUATION(componentRef, exp, source, attr)` — explicitly solved
    form `componentRef = exp` (currently no code path constructs this; see
    audit entry in `REPORTS/2026-04-17_BDAE_audit/audit.md`).
  - `RESIDUAL_EQUATION(exp, source, attr)` — `0 = exp` residual form;
    produced by `Causalize.residualizeEveryEquation`, not the frontend.
  - `ALGORITHM(size, alg, source, expand, attr)` — algorithm section.
    `size` is the number of equations the block stands in for; `expand`
    is inherited from `DAE.Expand` and tells the code generator whether
    to expand array crefs inside the body.
  - `WHEN_EQUATION(size, whenEquation, source, attr)` — ordinary `when`.
  - `STRUCTURAL_WHEN_EQUATION(size, whenEquation, source, attr)` — `when`
    whose body contains a `RECOMPILATION` or `AGENTIC_RECOMPILATION`
    operator, marking it as a trigger for VSS structural change.
  - `COMPLEX_EQUATION(size, left, right, source, attr)` — record-typed
    equality; `size` counts the scalarized fields.
  - `IF_EQUATION(conditions, eqnstrue, eqnsfalse, source, attr)` — each
    condition maps one-to-one to a branch in `eqnstrue`; `eqnsfalse` holds
    the else branch.
  - `FOR_EQUATION(iter, start, stop, body, source, attr)` — ranged loop
    equation. No frontend path currently emits it.
  - `DUMMY_EQUATION` — placeholder used when a NORETCALL cannot be lowered.
  - `ASSERT_EQUATION(condition, message, level, source)`.
  - `INITIAL_STRUCTURAL_STATE(initialState)` — records the initial mode
    name for a variable-structure system.
  - `BRANCH(ar, br)` — `Connections.branch(ar, br)`.
  - `STRUCTURAL_IF_EQUATION(ifEquation)` — DOCC if-equation preserved as a
    frontend `EQUATION_IF` so the runtime can replay the branch choice.
  - `STRUCTURAL_TRANSITION(fromState, toState, transitionCondition)` —
    VSS transition; a structural callback is generated from the condition.
    (Name is a historical typo preserved across the codebase.)
"""
@UniontypeDecl Equation

"""
  Condition-guarded block of [`WhenOperator`](@ref) statements.

  Single variant:

  - `WHEN_STMTS(condition, whenStmtLst, elsewhenPart)`:
    - `condition`: the when-condition expression.
    - `whenStmtLst`: list of `WhenOperator` statements to run on trigger.
    - `elsewhenPart`: chained `elsewhen`. Intentionally untyped: depending
      on the call site it can be `NONE()`, an `Option{WHEN_STMTS}`, a
      `SOME(WHEN_EQUATION)` (as produced by `lowerWhenEquation`), or plain
      `nothing`. Until those call sites converge, constraining the field
      type would be a breaking change.
"""
@UniontypeDecl WhenEquation

"""
  Statements permitted inside a `when` clause.

  Variants and their fields:

  - `ASSIGN(left, right, source)` — `left = right` fired on the trigger.
  - `REINIT(stateVar, value, source)` — `reinit(stateVar, value)`.
    `stateVar` is stored as a `DAE.CREF` (rather than a raw
    `ComponentRef`) to unify expression traversal.
  - `ASSERT(condition, message, level, source)`.
  - `TERMINATE(message, source)`.
  - `NORETCALL(exp, source)` — no-return call inside a `when`.
  - `RECOMPILATION(componentToChange, newValue)` — VSS structural change
    trigger; emits a recompile with the given component set to `newValue`.
    Only meaningful inside a `when` equation.
  - `AGENTIC_RECOMPILATION(componentsToChange, prompt, initialEquations)`
    — agent-driven recompilation. New values are determined at runtime by
    an external agent. `prompt` forwards a natural-language hint; the
    serialized `initialEquations` string supplies constraint equations the
    agent must honour.
"""
@UniontypeDecl WhenOperator

