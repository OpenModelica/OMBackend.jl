"""
  Backend intermediate representation (BDAE) for the OMBackend pipeline.

  A `BACKEND_DAE` is the lowered form of a flattened Modelica model. It splits
  variables into two pools (unknown states / algebraic, and known parameters /
  constants) and splits equations into ordered equations plus a bucket of simple
  alias equations that have already been removed from the main equation set.
"""
